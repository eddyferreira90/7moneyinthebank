package org.academia.bootcamp;

public class Lojadecenas {

    private int cena1;
    private int cena2;


    public Lojadecenas (int cena1, int cena2){
        this.cena1 = cena1;
        this.cena2 = cena2;
    }

    public void setCena1(int cena1) {
        this.cena1 = cena1;
    }

    public int getCena1() {
        return cena1;
    }

    public void setCena2(int cena2) {
        this.cena2 = cena2;
    }

    public int getCena2() {
        return cena2;
    }


}

